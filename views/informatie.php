  <h2><b>Informatie over de IT-Lympics 2018</b></h2>
  <hr /> 
  <p style="font-size:20px;">  In de week van maandag 29 januari 2018 organiseert Windesheim Flevoland, samen met het Almeerse bedrijfsleven, de IT-Lympics 2018. De IT-Lympics is een sportevenement voor eerstejaars Windesheimstudenten HBO-ICT en Associate Degree Software Development. Teams van 4 personen zullen met elkaar de strijd aangaan in diverse ICT-gerelateerde sportieve evenementen. Ook strijden de teams om de prijs voor de beste app en de beste video van het evenement.</p>


<p style="font-size:20px;">&nbsp;</p>
<p style="font-size:20px;">De jury, bestaande uit de directeur van Windesheim Flevoland, de hogeschoolhoofddocent ICT en de coordinator van de ICT-kring van de VBA, zullen de hackathon-opdrachten van de studenten beoordelen. Voor de winndende teams zijn mooie prijzen beschikbaar gesteld door de deelnemende bedrijven.</p>
<p style="font-size:20px;">De samenwerking met Almeerse bedrijven en de gemeente Almere staat centraal. Studenten komen op een leuke manier in contact met de IT-bedrijven die Almere rijk is. Bedrijven kunnen zich anderzijds presenteren als potenti&euml;le opdrachtgever voor &eacute;&eacute;n van de comakerships die studenten tijdens hun studie bij een bedrijf volgen.</p>

<p style="font-size:20px;">De IT-Lympics 2018 worden georganiseerd door Windesheim Flevoland in samenwerking met de Vereniging Bedrijfskring Almere en de Gemeente Almere.</p></p>




